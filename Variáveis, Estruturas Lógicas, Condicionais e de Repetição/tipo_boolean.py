'''
Tipo Boolean

2 constantes. True ou False

Sempre com a inicial Maiúscula
'''

ativo = True
print(ativo)

# NEGAÇÃO (not)
# SE O VALOR FOR VERDADEIRO O RESULTADO SERÁ FALSE
# SE O VALOR FOR FALSO O RESULTADO SERÁ TRUE

ativo = False
print(not ativo)

# OU (or)
# True or True = True
# True or False = True
# False or True = True
# False or False = False

logado = True
print(ativo or logado)

# E (and)
# Ambos os valores devem ser verdadeiro
# True and True = True
# False and False = False
# True and False = False
# False and True= False

print(ativo and logado)