"""
Tipo de Float 
Tipo Real, tipo decimal

O separador de casas decimais é . (ponto) e não , (virgula)
"""

valor = 1.44
print(valor)
print(type(valor)) #Descobrir o tipo da variável

# Outras formas

valor1, valor2 = 5.39, 9
print(valor1)
print(type(valor1))
print(valor2)
print(type(valor2))

#Transformar float em int

resultado = (int(valor))
print(resultado)
print(type(resultado))